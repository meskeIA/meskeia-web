// Funci√≥n para obtener t√©rminos por letra inicial
export function getTerminosPorLetra(letra: string): Termino[] {
  return TERMINOS.filter(t =>
    t.termino.toLowerCase().startsWith(letra.toLowerCase())
  );
}

// Funci√≥n para buscar t√©rminos
export function buscarTerminos(query: string): Termino[] {
  const q = query.toLowerCase();
  return TERMINOS.filter(t =>
    t.termino.toLowerCase().includes(q) ||
    t.traduccion?.toLowerCase().includes(q) ||
    t.definicion.toLowerCase().includes(q)
  );
}

// Obtener todas las letras que tienen t√©rminos
export function getLetrasDisponibles(): string[] {
  const letras = new Set(TERMINOS.map(t => t.termino[0].toUpperCase()));
  return Array.from(letras).sort();
}

// Obtener t√©rminos por categor√≠a
export function getTerminosPorCategoria(categoria: Categoria): Termino[] {
  return TERMINOS.filter(t => t.categoria === categoria);
}
