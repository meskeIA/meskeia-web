// Funci√≥n auxiliar para buscar minerales
export function searchMinerals(query: string): Mineral[] {
  const q = query.toLowerCase();
  return MINERALS.filter(m =>
    m.nombre.toLowerCase().includes(q) ||
    m.formulaQuimica.toLowerCase().includes(q) ||
    m.usos.some(u => u.toLowerCase().includes(q)) ||
    m.colores.some(c => c.toLowerCase().includes(q))
  );
}
